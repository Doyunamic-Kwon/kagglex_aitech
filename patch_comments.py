counts = {old: 0 for old, _ in R}

def patch_ipynb(path):
    nb = nbformat.read(path, as_version=4)
    for c in nb.cells:
        if c.cell_type != "code":
            continue
        for old, new in R:
            if old in c.source:
                c.source = c.source.replace(old, new)
                counts[old] += c.source.count(new) if False else 1
    nbformat.write(nb, path)

def patch_text(path):
    with open(path, encoding="utf-8") as f:
        t = f.read()
    for old, new in R:
        if old in t:
            t = t.replace(old, new)
            counts[old] += 1
    with open(path, "w", encoding="utf-8") as f:
        f.write(t)

for p in ["disaster_tweets.ipynb", "commonlit_readability.ipynb"]:
    patch_ipynb(p)
for p in ["build_notebook.py", "build_notebook_commonlit.py"]:
    patch_text(p)

missed = [old for old, c in counts.items() if c == 0]
